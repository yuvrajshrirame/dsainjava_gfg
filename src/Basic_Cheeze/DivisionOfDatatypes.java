package Basic_Cheeze;

public class DivisionOfDatatypes {
    public static void main(String[] args) {

        double x = 5/2; // 2
        double y = 5.0/2.0; // 2.5
        double z = 5/2.0; // 2.5
        double w = 5.0/2; // 2.5

        System.out.println(x + ", " + y + ", " + z + ", " + w);
    }
}
